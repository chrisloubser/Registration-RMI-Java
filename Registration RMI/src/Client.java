        String cell = jTextField5.getText();
        String degree = jComboBox1.getSelectedItem()+"";
        
        //validation
        if (validateData() == false) {
           JOptionPane.showMessageDialog(null,"One or More fields remain empty",   "Error",JOptionPane.ERROR_MESSAGE); 
        }

        try {
            //registry to connect to server
            Registry myRegistry = LocateRegistry.getRegistry("127.0.0.1", 1099);
            //stub to call interface method
            ConnectInterface stub = (ConnectInterface) myRegistry.lookup("ConnectInterface");
            //insert into stub
            stub.insert(ID, name, surname, age, cell, degree);
        } catch (Exception e) {
            System.out.println(e);
        }
        //call clear data
        clearData();
        
        
        
        
